package operators;

public class OperatorDemo {
	public static void main(String[] args) {
		
		// Arithmatic
		// + - * / %

		int i = 10;
		int j = 6;
		
		System.out.println(i + j ); //16
		System.out.println(i - j ); // 4
		System.out.println(i * j ); // 60
		System.out.println(i / j ); // 1
		System.out.println(i % j ); // 4
		
		
	}
}
